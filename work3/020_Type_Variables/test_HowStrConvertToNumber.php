<?php

  //進行數學運算時，若字串為純數字，會自動轉換為整數，
  $x = 1 + "10";  // 11
  echo $x;
  echo "<BR><HR>";
  
  $x = "11" + 1;  // 12
  echo $x;
  echo "<BR><HR>";
  
  //若先讀取到字串 則後方皆視為字串，同等於0
  $x = 1 + "x10"; // 1
  echo $x;
  echo "<BR><HR>";
  
  //若先收到數字，則數字部分會記錄下來直到遇到字串
  $x = 1 + "10x"; // 11
  echo $x;
  echo "<BR><HR>";
  
  $x = 1 + "11.5x";  // 12.5;
  echo $x;
  echo "<BR><HR>";
  
  $x = 1 + "x11.5";  // 1;
  echo $x;
  echo "<BR><HR>";
  
?>